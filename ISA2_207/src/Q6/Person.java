package Q6;

public class Person {

		private String name;
		private int age;

			//Constructor
			Person(String name, int age)
			{
			this.name=name;
			this.age=age;
			}
			public String getName() {
				return name;
			}
			public void setName(String name) {
				this.name=name;
			}

			public int getAge() { 
				return age;
			}
			public void getAge(int age) {
				this.age=age;
			
	}

}
